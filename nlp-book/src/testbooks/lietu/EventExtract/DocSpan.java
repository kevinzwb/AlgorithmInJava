package testbooks.lietu.EventExtract;

/**
 * 
     * 用来操作AddressType.type类和并给其定义规则
     * @author Administer
     * @2010-3-18
 */

public class DocSpan {
	public int length;
	public DocType type;
	
	public DocSpan(int l,DocType t)
	{
		length = l;
		type = t;
	}
	
	public String toString()
	{
		return type+":"+length;
	}
}
